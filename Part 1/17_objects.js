/*
OBJECTS IN JAVASCRIPT
    > arrays are good but not sufficient
    > for real world data
    > key-value pairs
    > object's don't have index
*/

// HOW TO CREATE
const person = {
    name: 'Prince Sunsara',
    age: 20,
    city: 'Patan'
};
console.log(person)

// HOW TO ACCESS DATA
console.log(person.name)

// HOW TO ADD KEY-VALUE PAIR IN OBJECT
person.gender = 'male';
console.log(person)
